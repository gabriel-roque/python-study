#TESTANDO OS TIPOS PRIMITIVOS

#REAL
n = float(input('Digite o valor: '))
print(type(n))
#TEXTO
n = str(input('Digite o valor: '))
print(type(n))
#INTEIRO
n = int(input('Digite o valor: '))
print(type(n))
#LÓGICO TRUE AND FALSE
n = bool(input('Digite o valor: '))
print(type(n))


#SE HOUVER VALOR ELE RETORNA TRUE, SE NÃO HOUVER ELE RETORNA FALSE
n = bool(input('Digite o valor: '))
print(n)