lista = ['Claudio', 'Jose', 'Maria', 'Beltrano', 'Joao', 'Fulano', 'Ciclano']
print(lista)
lista.reverse() #investe a sequencia original
print(lista)

lista.sort() #ordena de forma acendente
print(lista)

lista.sort(reverse=True) #ordena de forma descendente // de tras para frente
print(lista)